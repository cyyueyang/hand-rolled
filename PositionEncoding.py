def precompute_freq_cis(args: ModelArgs):
    dim = args.qk_rope_head_dim
    seq_len = args.max_seq_len
    beta_fast = args.beta_fast
    beta_slow = args.beta_slow
    base = args.rope_theta
    factor = args.rope_factor

    def find_correction_dim(num_rotations: int, base: float, dim: int, max_seq_len: int):
        return dim * math.log(max_seq_len / (2 * math.pi * num_rotations)) / (2 * math.log(base))

    def find_correction_range(low_rot: int, high_rot: int, base: float, dim: int, max_seq_len: int):
        low = math.floor(find_correction_dim(low_rot, base, dim, max_seq_len))
        high = math.ceil(find_correction_dim(high_rot, base, dim, max_seq_len))
        return max(low, 0), min(high, dim-1)

    def linear_ramp_factor(min, max, dim):
        if min == max:
            max += 0.001

        linear_func = (torch.arange(dim, dtype=torch.float32) - min) / (max - min)
        ramp_func = linear_func.clamp(0, 1)
        return ramp_func

    freqs = 1.0 / (base ** (torch.arange(0, dim, 2, dtype=torch.float32) / dim))
    if seq_len > args.original_seq_len:
        low, high = find_correction_range(beta_slow, beta_fast, base, dim, args.original_seq_len)
        smooth = 1 - linear_ramp_factor(low, high, dim // 2)
        freqs = freqs / factor * (1 - smooth) + freqs * smooth

    t = torch.arange(seq_len)
    freqs = torch.outer(t, freqs)
    cis = torch.polar(torch.ones_like(freqs), freqs)
    return cis

def apply_rotary_emb(x: torch.Tensor, freqs_cis: torch.Tensor):
    dtype = x.dtype
    x = torch.view_as_complex(x.view(*x.shape[:-1], -1, 2))
    freqs_cis = freqs_cis.view(1, x.size(1), 1, x.size(-1))
    y = torch.view_as_real(x * freqs_cis).flatten(3)
    return y.to(dtype)



